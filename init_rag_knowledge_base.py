def initialize_knowledge_base():
    """初始化知识库"""
    try:
        from api_service.core.vector_db import get_vector_db_manager
        from api_service.core.rag_config import rag_settings

        logger.info("开始初始化RAG向量知识库...")

        # 获取向量数据库管理器
        vector_db = get_vector_db_manager()

        # 检查知识库是否已存在
        stats = vector_db.get_collection_stats()
        if stats.get('count', 0) > 0:
            logger.info(f"知识库已存在，包含 {stats['count']} 个文档")
            return

        # 添加所有知识
        total_added = 0
        for category, documents in TCM_KNOWLEDGE_BASE.items():
            category_count = 0

            for doc_data in documents:
                # 准备文档数据
                text = doc_data['content']
                metadata = {
                    'title': doc_data['title'],
                    'category': category,
                    'source': doc_data['metadata'].get('source', '未知'),
                    'author': doc_data['metadata'].get('author', '未知'),
                    'tags': doc_data['metadata'].get('tags', []),
                    'description': f"{doc_data['title']} - {doc_data['content'][:100]}..."
                }

                # 添加文档
                success = vector_db.add_documents(
                    texts=[text],
                    metadatas=[metadata],
                    ids=[f"{category}_{doc_data['title']}"]
                )

                if success:
                    category_count += 1
                    total_added += 1

            logger.info(f"添加 {category_count} 个文档到 {category} 类别")

        logger.info(f"知识库初始化完成，共添加 {total_added} 个文档")

        # 显示统计信息
        final_stats = vector_db.get_collection_stats()
        logger.info(f"知识库统计: {final_stats}")

        return {
            'success': True,
            'total_added': total_added,
            'stats': final_stats
        }

    except Exception as e:
        logger.error(f"知识库初始化失败: {e}")
        return {
            'success': False,
            'error': str(e)
        }


def main():
    """主函数"""
    print("=" * 80)
    print("RAG向量知识库初始化")
    print("RAG Vector Knowledge Base Initialization")
    print("=" * 80)

    result = initialize_knowledge_base()

    if result['success']:
        print(f"\n✓ 知识库初始化成功")
        print(f"✓ 共添加 {result['total_added']} 个文档")
        print(f"✓ 统计信息: {result['stats']}")
        print("\n知识库包含以下类别:")
        for category in TCM_KNOWLEDGE_BASE.keys():
            count = len(TCM_KNOWLEDGE_BASE[category])
            print(f"  - {category}: {count} 个文档")
    else:
        print(f"\n✗ 知识库初始化失败: {result['error']}")

    print("=" * 80)


if __name__ == "__main__":
    main()